package pragraClass.randomPractice;

public class SwapNumber {
    public static void main(String[] args) {
        int a =10;
        int b =20;
        int c;
        System.out.println("initially we have a = "+a+" and b = "+b);
        c=a;
        a=b;
        b=c;
        System.out.println("after swapping we have a = "+a+" and b = "+b);
    }
}
